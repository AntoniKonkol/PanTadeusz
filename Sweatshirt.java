

public class Sweatshirt
{
   private String brand;
   private int price;
   
   void setBrand(String brand){
       this.brand = brand;
    }
   void setPrice(int price){
       this.price = price;
    }
   public int getPrice(){
       return this.price;
    }
   public String getBrand(){
       return this.brand;
    }
}
